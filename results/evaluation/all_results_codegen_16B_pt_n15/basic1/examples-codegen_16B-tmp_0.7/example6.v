// Design a module that assigns the output to the input
module wire_assign( input in, output out );
// assign out to in
wire_assign( in, out );
// assign out to in
uint8_t in = 5;
// assign in to out
wire_assign( in, out );
// assign in to out
uint8_t in[ 10 ];
// assign in to out
wire_assign( in, out );
// assign in to out
uint8_t in[ 10 ][ 10 ];
// assign in to out
wire_assign( in, out );
// assign in to out
uint8_t in[ 10 ][ 10 ][ 10 ];
// assign in to out
wire_assign( in, out );endmodule

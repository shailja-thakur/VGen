// Design a module that implements an AND gate

module and_gate( 
    input a, 
    input b, 
    output out );

    // assing the AND of a and b to out
        // input port INPUT_A = a
        // input port INPUT_B = b
        // output port OUTPUT = out
out = a & b;

EndDesign

